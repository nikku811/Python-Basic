# input(): takes input as string
a = input("Enter first no. : ")  # a=5
b = input("Enter second no. : ") # b=6
print(a + b) # output:56

# to solve this issue we use 'int()'function for type convertion
a = int(input("Enter first no. : "))  # a=5
b = int(input("Enter second no. : ")) # b=6
print(a + b) # output:11