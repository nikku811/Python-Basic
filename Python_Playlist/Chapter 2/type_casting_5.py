# Type() and Typecasting

# type(): used to find the type of data types which that variable hold

# Emaple
a = 35.7        # float value
b = 34          # int value
c = "345.66"    # string value
d = type(a)     # type of "a" assign to "d"
e = type(int(a)) # conerting into 'int'

print(type(a))   # float
print(type(b))   # int
print(type(c))   # str
print(d)   # float
print(e)   # int
