# Data types example
a = 10              # int
b = 5.5             # float
c = "Hello"         # string
d = [1, 2, 3]       # list
e = (4, 5, 6)       # tuple
f = {"name": "Ram"} # dictionary
g = {1, 2, 3}       # set
h = True            # boolean
print(type(a), type(b), type(c), type(d), type(e), type(f), type(g), type(h))
print(1 + 2)
print("Hello " + "World")
print(True and False)
print(4.0 / 2.0)
